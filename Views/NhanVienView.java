    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        NhanVien nv = new NhanVien();
        nv.setId(lblId.getText());
        nv.setMa(txtMa.getText());
        nv.setTen(txtTen.getText());
        nv.setTenDem(txtTenDem.getText());
        nv.setHo(txtHo.getText());
        nv.setGioiTinh(txtGioiTinh.getText());
        nv.setNgaySinh(txtNgaySinh.getText());
        nv.setDiaChi(txtDiaChi.getText());
        nv.setSDT(txtSdt.getText());
        nv.setMatKhau(txtMatKhau.getText());
        nv.setTrangThai(txtTrangThai.getText());
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn update dòng này ");
        if (confirm == JOptionPane.YES_OPTION) {
            service.update(nv);
            nvr = service.getAll();
            loadData();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tbNhanVien.getSelectedRow();
        String id = tbNhanVien.getValueAt(row, 0).toString();
        if (service.remove(id) != null) {
            nvr = service.getAll();
            loadData();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien nv = addData();
        if (service.insert(nv) != null) {
            nvr = service.getAll();
            loadData();
        }
    }//GEN-LAST:event_btnThemActionPerformed

