    public void loadData() {
        model = (DefaultTableModel) tbNhanVien.getModel();
        model.setRowCount(0);
        for (NhanVienResponse nhanVienResponse : nvr) {
            model.addRow(nhanVienResponse.toDataRow());
        }
    }

    public NhanVien addData() {
        NhanVien nv = new NhanVien();
        nv.setId(lblId.getText());
        nv.setMa(txtMa.getText());
        nv.setTen(txtTen.getText());
        nv.setTenDem(txtTenDem.getText());
        nv.setHo(txtHo.getText());
        nv.setGioiTinh(txtGioiTinh.getText());
        nv.setNgaySinh(txtNgaySinh.getText());
        nv.setDiaChi(txtDiaChi.getText());
        nv.setSDT(txtSdt.getText());
        nv.setMatKhau(txtMatKhau.getText());
        nv.setTrangThai(txtTrangThai.getText());
        return nv;
    }

