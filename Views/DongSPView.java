    public void loadData() {
        model = (DefaultTableModel) tbDongSP.getModel();
        model.setRowCount(0);
        for (DongSPResponse dongSPResponse : dspr) {
            model.addRow(dongSPResponse.toDataRow());
        }
    }

    public DongSP add() {
        DongSP dsp = new DongSP();
        dsp.setId(lblId.getText());
        dsp.setMa(txtMa.getText());
        dsp.setTen(txtTen.getText());

        return dsp;
    }

