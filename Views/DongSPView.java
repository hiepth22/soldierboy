    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        DongSP dsp = add();
        if (service.insert(dsp) != null) {
            dspr = service.getAll();
            loadData();
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        DongSP dsp = new DongSP();
        dsp.setId(lblId.getText());
        dsp.setMa(txtMa.getText());
        dsp.setTen(txtTen.getText());
        service.update(dsp);
        dspr = service.getAll();
        loadData();
        JOptionPane.showMessageDialog(this, "Sửa thành công");
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tbDongSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDongSPMouseClicked
        int row = tbDongSP.getSelectedRow();
        lblId.setText(tbDongSP.getValueAt(row, 0).toString());
        txtMa.setText(tbDongSP.getValueAt(row, 1).toString());
        txtTen.setText(tbDongSP.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tbDongSPMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tbDongSP.getSelectedRow();
        String id = tbDongSP.getValueAt(row, 0).toString();
        if (service.remove(id) != null) {
            dspr = service.getAll();
            loadData();
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

