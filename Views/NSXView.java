    private void tbNSXMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNSXMouseClicked
        int row = tbNSX.getSelectedRow();
        lblID.setText(tbNSX.getValueAt(row, 0).toString());
        txtMa.setText(tbNSX.getValueAt(row, 1).toString());
        txtTen.setText(tbNSX.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tbNSXMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        NSX nsx = setData();
        if (service.insert(nsx) != null) {
            nxsr = service.getAllNSX();
            loadData();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        NSX nsx = new NSX();
        nsx.setId(lblID.getText());
        nsx.setMa(txtMa.getText());
        nsx.setTen(txtTen.getText());
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn update dòng này ?");
        if (confirm == JOptionPane.YES_OPTION) {
            service.update(nsx);
            nxsr = service.getAllNSX();
            loadData();
            JOptionPane.showMessageDialog(this, "Update thành công");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tbNSX.getSelectedRow();
        String id = tbNSX.getValueAt(row, 0).toString();
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa dòng này ?");
        if (confirm == JOptionPane.YES_OPTION) {
            service.remove(id);
            nxsr = service.getAllNSX();
            loadData();
            JOptionPane.showMessageDialog(this, "Đã xóa");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

