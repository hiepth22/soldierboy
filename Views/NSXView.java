    public void loadData() {
        model = (DefaultTableModel) tbNSX.getModel();
        model.setRowCount(0);
        for (NSXResponse nSXResponse : nxsr) {
            model.addRow(nSXResponse.toDataRow());
        }
    }

    public NSX setData() {
        NSX nsx = new NSX();
        nsx.setId(lblID.getText());
        nsx.setMa(txtMa.getText());
        nsx.setTen(txtTen.getText());

        return nsx;
    }

