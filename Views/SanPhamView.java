    public void loadData() {
        model = (DefaultTableModel) tbSanPham.getModel();
        model.setRowCount(0);
        for (SanPhamResponse sanPhamResponse : spr) {
            model.addRow(sanPhamResponse.toDataRow());
        }
    }

    public SanPham addData() {
        SanPham sp = new SanPham();
        sp.setId(lblID.getText());
        sp.setMa(txtMa.getText());
        sp.setTen(txtTen.getText());
        return sp;
    }

