    private void tbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSanPhamMouseClicked
        int row = tbSanPham.getSelectedRow();
        lblID.setText(tbSanPham.getValueAt(row, 0).toString());
        txtMa.setText(tbSanPham.getValueAt(row, 1).toString());
        txtTen.setText(tbSanPham.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tbSanPhamMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        SanPham sp = addData();
        if (service.insert(sp) != null) {
            spr = service.getAllSanPham();
            loadData();
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        SanPham sp = new SanPham();
        sp.setId(lblID.getText());
        sp.setMa(txtMa.getText());
        sp.setTen(txtTen.getText());
        service.update(sp);
        spr = service.getAllSanPham();
        loadData();
        JOptionPane.showMessageDialog(this, "Update thành công");
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tbSanPham.getSelectedRow();
        String id = tbSanPham.getValueAt(row, 0).toString();
        if (service.remove(id) != null) {
            spr = service.getAllSanPham();
            loadData();
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

